            <div class="row">
                <h3 class="text-base-color fw-semibold">Transfer Credit</h3>
                <p>There are two types of transfer of credit from outside NEWUU.</p>
                <ul class="list-style-03 ps-3 text-dark">
                    <li>NEWUU sponsored student mobility programs</li>
                    <li>Student initiated study abroad</li>
                </ul>
                <p>All courses for transfer from outside Higher Education Institutions’ are subject to program-specific
                    guidelines, Credit for courses taken at other accredited Higher Education Institutions (hereinafter –
                    HEI) may be transferred to a student’s University record. Only courses with English language of
                    instruction can be transferred, unless it is a language course.</p>
                <br>
                <p>1) Before starting the process for admission, the student must discuss this initiative with the
                    Department Chair and their advisor.</p>
                <p>2) The student should review the academic calendar of the university where the student wants to study
                    to ensure that the academic calendar dates match NEWUU’s to avoid loss of study time.</p>
                <p> 3) If this study is at the student’s initiative and the student will miss a semester of courses at NEWUU,
                    the student must complete a Withdrawal for Study form. Prior to their return to NEWUU they must
                    complete a Return to Study form.</p>
                <p>  4) To transfer credits from another university, a School submits a completed “External Transfer of
                    Credits” form and an official transcript confirming the credits awarded and the grade received to the
                    Office.</p>
                <p> 5) In cases where the status of a course completed at another institution is in doubt, the Dean shall
                    make a determination, which will govern all future instances in which a student seeks to transfer
                    credits from the same courses from the same institution.</p>
                <p>     6) Only grades of <b>C or above</b> awarded at the other University will be awarded. Transfer credit is not
                    awarded for courses in which the student earned a grade lower than C.</p>
                <p>7) All transfer credits will be annotated as “TC” on the student’s transcript and are not included in
                    computing the student’s CGPA or GPA at NEWUU.</p>
                <br>
                <p>Courses taken at other institutions, which are graded on a Pass/Fail system are not eligible for
                    transfer for letter-graded University courses.</p>
                <p>In case of transfer of credit based upon approved Academic Mobility programs, up to 36 ECTS
                    credits may be transferred. Discretion may be made with approval of the Dean for a student who
                    was on an approved Academic Mobility program.</p>
                <p>Transfer credit must be approved by the Vice Dean of the School.</p>
                <p>Credits earned in other HEI during the study spent at the University shall be processed in
                    accordance with appropriate internal regulations of the University.</p>
                <p>Unless a special credit transfer agreement/regulation applies, students studying for a degree at NewUU
                    may apply for a transfer of the specified maximum amount of credits based on their degree level:</p>
                <br>
                <ul class="list-style-04 ps-3 text-dark">
                    <li>Bachelor’s students – up to 36 ECTS credits. All transfer requests must be made and
                        completed no later than the end of a student's junior year.</li>
                    <li>Master’s students in one-year programs – up to 16 ECTS credits. All transfer requests
                        must be made and completed no later than the end of a student's first semester at
                        NewUU.</li>
                    <li>Master’s students in two-year program - up to 30 ECTS credits. All transfer requests
                        must be made and completed no later than the end of a student's first year.</li>
                    <li>Master’s or Doctoral students taking a year abroad module as part of their degree
                        program may apply for transfer of a year’s worth of credits (30 US / 60 ECTS). All
                        transfer requests must be made and completed immediately upon return to NewUU.</li>
                    <li>Doctoral students – up to 15 US course credits (30 ECTS course credits). Different
                        limits may be established by departmental doctoral regulations for non-coursework
                        program components. All transfer requests must be made and completed immediately
                        upon return to NewUU. Credit will be awarded by NewUU upon receipt of official
                        transcript from the other University.</li>
                </ul>
                <br>
                <p>As a general rule, transferred credits must not have been used to satisfy degree requirements for any
                    other degree. However, this rule, as well as the limit of transferred credits, may be modified by (a)
                    special cooperation agreements between NewUU and other universities; (b) specific program
                    regulations (if permitted by respective program accreditation); or (c) for undergraduate students upon
                    approval of the Dean of School.
                </p>
                <p>All transfer of credit requests that are not regulated in special cooperation agreements are subject to
                    the approval of the Dean of School and the Vice-Rector for Academic Affairs upon a recommendation
                    by the head of the relevant department or program.</p>
                <br>
                <i class="text-base-color-2">Application Procedure for</i>
                <ul class="list-style-04 ps-3 text-dark">
                    <li>Applications for transfer of academic credit must be submitted to the Academic Affairs
                        before the beginning of the registration period.

                    </li>
                    <li>Applicants must contact the department/program coordinator/ head to determine which
                        of their previous studies and other learning experiences are likely to be relevant</li>
                    <li>If seeking credit transfer from another educational institution, the following must be
                        attached:
                        <ul>
                            <li>an original, official transcript must (or official certified copy) to be attached,
                                showing the courses and grades achieved.</li>
                            <li>A course outline published by the other educational institution also needs to
                                be attached.</li>
                        </ul>
                    </li>
                    <li>Applicants are responsible for providing all relevant documentation, for example, an
                        official transcript of results, copy of the course structure and outline or syllabus of the
                        course.</li>
                    <li>Undocumented applications for academic credit will not be considered.</li>
                    <li>All transfer of credit requests is subject to the approval of the Dean of School (in case
                        of bachelors’ programs), the Vice-Rector for Academic Affairs (in case of masters’
                        and doctoral programs) upon a recommendation by the head of the relevant department
                        or program</li>
                    <li>Applicants for academic credit transfer may be required to attend an interview or to
                        undergo additional assessment as the head of the respective academic unit may
                        determine</li>
                </ul>
                <br>
                <p>The Application for Transfer of Credit can be submitted electronically. The form should be requested
                    at the Academic Affairs.</p>
