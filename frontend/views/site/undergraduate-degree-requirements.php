                <h3 class="fw-semibold text-base-color">Undergraduate Degree Requirements</h3>
                <p>University degrees are awarded by the Rector based on the fulfillment of degree requirements. The degree requirements are specified in the relevant program Specification in the corresponding Program Handbooks. All Students should make satisfactory progress towards the degree, entailing:</p>
                <span>A) Registration for the duration of the program.</span><br>
                <span>B) Attendance and participation in courses is required by the university attendance which is outlined above.</span><br>
                <span>C) Maintaining regular contact with an academic supervisor and student advisors.</span><br>
                <span>D) Passing exams and academic assignments with satisfactory grades.</span><br>
                <span>E) Satisfying the final degree award criteria with respect to the number and type of course credits taken with respect to core, optional and elective courses</span><br>
                <span>F) Undergraduate students are expected to accumulate on average 30 US / 60 ECTS credits per year to complete their studies successfully. Under exceptional circumstances, students may take longer to complete their studies. In this case, the Office of Academic Affairs should inform and provide the list of such students to the office of Rector. The maximum duration of study allowed in undergraduate programs, excluding leave of absence, is eight years.</span><br>
                <span>G) For undergraduate students, the submission of a capstone project or paper as defined by the relevant program accreditation documents is also required and must be of satisfactory quality. After completing all degree requirements except for the written component, the submission of the written component should take place prior to the final grade submission for end-of-term reporting and graduation requirements. The time period must be specified by the department/program.</span><br>
                <span>H) INTERNSHIP: Students must complete an approved internship to graduate; Students in bachelor’s programs must attain an overall GPA of 2.0 at the end of each academic year. Bachelor’s students whose overall GPA is below 2.0 at the end of the academic year will be retained in the appropriate year of study. They are required to study all failed courses of the previous academic year. Students with academic debt based on the results of the Academic Year will not be excluded from studies. In this case, the student may not claim any scholarship during the following Academic Year. After one Academic Year students may apply for the 1-year merit-based scholarship or 4-year scholarship based on the “Unified Register of Social Protection” only.</span><br>
                <span>I) Students Achieving a Grade Point Average (GPA) of 2.0 and above will proceed to the next academic year by the Decree of the Rector. Students whose overall GPA is over 2.0 but have failed courses must retake the courses in subsequent academic years and will pay their contractual fees accordingly. The contract does not limit the number- of retakes of a course.</span><br>
