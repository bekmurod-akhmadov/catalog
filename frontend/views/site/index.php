                                                    <p>1. Complete the online application and submit it to the Academic Affairs office no later than the 10th week of class in the Spring semester. Applications will not be accepted after the deadline.
                                                        <br>
                                                        2. Upon receiving the applications, the Office of Academic Affairs will conduct an initial screening to ensure they meet the minimum university requirements for program changes. Incomplete or ineligible applications will be rejected at this
                                                        stage.<br>
                                                        3. Approved applications will then be forwarded to the respective department heads for further consideration. The department heads will review each application against the specific minimum requirements of the receiving department.<br>
