                                                        a) the student has covered at least 70% of all courses of the new program within the normal program duration and curriculum approved in the Program Handbook; all missed courses of the new program should be retaken as soon as
                                                        possible;<br>
                                                        b) the student has demonstrated good academic performance with a CGPA 3 or above;<br>
